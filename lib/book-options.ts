export const getSubjectsByDomain = (domain: string) => {
  if (!domain || !(domain in SUBJECT_OPTIONS_BY_DOMAIN)) {
    return [] as string[];
  }
  return [...SUBJECT_OPTIONS_BY_DOMAIN[domain as Domain]];
};

